SELECT IDCOOPERATIVA AS ID_COOPERATIVA
    , cooperativa.NOME AS COOPERATIVA_NOME
    , cooperativa.ENDERECO
    , cooperativa.CIDADE
    , cooperativa.LATITUDE
    , cooperativa.LONGITUDE
    , cooperativa.SIGLA
    , cooperativa.ID_AREA_ATUACAO AS AREA_ATUACAO_ID
    , a_atuacao.DESC_AREA_ATUACAO AS AREA_ATUACAO_DESC
    , cooperativa.ID_LOCALIZACAO AS LOCALIZACAO_ID
    , local.DESC_LOCALIZACAO AS LOCALIZACAO_DESC
    , cooperativa.ID_TIPO_ORGANIZACAO AS TIPO_ORGANIZACAO_ID
    , t_organizacao.NOME_TIPO AS TIPO_ORGANIZACAO_NOME
    , cooperativa.ANO_INICIO
FROM CDT_COOPERATIVA cooperativa
LEFT JOIN CDT_AREA_ATUACAO a_atuacao
ON a_atuacao.IDAREAATUACAO = cooperativa.ID_AREA_ATUACAO
LEFT JOIN CDT_TP_ORGANIZACAO t_organizacao
ON cooperativa.ID_TIPO_ORGANIZACAO = t_organizacao.IDTIPOORGANIZACAO
LEFT JOIN CDT_LOCALIZACAO local
ON local.IDLOCALIZACAO = cooperativa.ID_LOCALIZACAO