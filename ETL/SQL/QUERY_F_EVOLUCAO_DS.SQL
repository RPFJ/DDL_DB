 SELECT 
 ID_EMPREENDIMENTO
 , DATE_ADD(DATE(COALESCE(data, current_timestamp)), INTERVAL -2 MONTH)  as DT_REGISTRO
 , ID_REDE_ECONOMICA
 , ID_GERACAO_RENDA
 , n_homens AS VL_N_HOMENS
 , n_mulheres AS VL_N_MULHERES
 , CAST(computador AS CHAR) AS VL_ACESSO_COMPUTADOR
 , CAST(internet AS CHAR) AS VL_ACESSO_INTERNET
 , 'E' AS FG_STATUS_ETL 
 
 FROM REL_EVOLUCAO AS evolucao;