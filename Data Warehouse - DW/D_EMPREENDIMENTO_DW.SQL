CREATE TABLE D_EMPREENDIMENTO_DW
(
    SK_EMPREENDIMENTO     NUMBER        NOT NULL,
    ID_EMPREENDIMENTO     NUMBER        NOT NULL,
    NOME_EMPREENDIMENTO   VARCHAR2(100) NOT NULL,
    ENDERECO              VARCHAR2(500) NOT NULL,
    CIDADE                VARCHAR2(100) NOT NULL,
    LATITUDE              NUMBER(23, 15),
    LONGITUDE             NUMBER(23, 15),
    SIGLA                 VARCHAR2(20),
    ID_AREA_ATUACAO       NUMBER        NOT NULL,
    DESC_AREA_ATUACAO     VARCHAR2(150) NOT NULL,
    ID_LOCALIZACAO        NUMBER        NOT NULL,
    DESC_LOCALIZACAO      VARCHAR2(500) NOT NULL,
    ID_TIPO_ORGANIZACAO   VARCHAR2(100) NOT NULL,
    DESC_TIPO_ORGANIZACAO VARCHAR2(100) NOT NULL,
    ANO_INICIO            NUMBER        NOT NULL,
    DT_INCLUSAO_DW        DATE DEFAULT SYSDATE,
    DT_ALTERACAO_DW       DATE,

    CONSTRAINT PK_D_EMPREENDIMENTO_DW PRIMARY KEY (SK_EMPREENDIMENTO)
);

CREATE INDEX IDX_D_EMPREENDIMENTO_DW ON D_EMPREENDIMENTO_DW (ID_EMPREENDIMENTO, ID_AREA_ATUACAO, ID_TIPO_ORGANIZACAO) TABLESPACE TBP_BI_USER_IDX;
CREATE SEQUENCE SEQ_D_EMPREENDIMENTO INCREMENT BY 1;