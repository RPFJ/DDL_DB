CREATE TABLE D_COOPERATIVA_DW
(
    SK_COOPERATIVA        NUMBER         NOT NULL,
    COOPERATIVA_ID        NUMBER         NOT NULL,
    COOPERATIVA_NOME      VARCHAR2(100)  NOT NULL,
    ENDERECO              VARCHAR2(500)  NOT NULL,
    CIDADE                VARCHAR2(100)  NOT NULL,
    LATITUDE              NUMBER(20, 10) NOT NULL,
    LONGITUDE             NUMBER(20, 10) NOT NULL,
    SIGLA                 VARCHAR2(20)   NOT NULL,
    AREA_ATUACAO_ID       NUMBER         NOT NULL,
    AREA_ATUACAO_DESC     VARCHAR2(100)  NOT NULL,
    LOCALIZACAO_ID        NUMBER         NOT NULL,
    LOCALIZACAO_DESC      VARCHAR2(500)  NOT NULL,
    TIPO_ORGANIZACAO_ID   NUMBER         NOT NULL,
    TIPO_ORGANIZACAO_NOME VARCHAR2(100)  NOT NULL,
    ANO_INICIO            NUMBER         NOT NULL,
    DT_INCLUSAO_DW        DATE DEFAULT SYSDATE,
    DT_ALTERACAO_DW       DATE,

    CONSTRAINT PK_D_COOPERATIVA_DW PRIMARY KEY (SK_COOPERATIVA)
);

CREATE INDEX IDX_D_COOPERATIVA_DW ON D_COOPERATIVA_DW (COOPERATIVA_ID, AREA_ATUACAO_ID, TIPO_ORGANIZACAO_ID) TABLESPACE TBP_BI_USER_IDX;
CREATE SEQUENCE SEQ_D_COOPERATIVA INCREMENT BY 1;